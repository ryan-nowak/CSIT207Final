CREATE DATABASE media;

use media;

CREATE TABLE TELEVISION (
	TV_ID INT(11) UNSIGNED AUTO_INCREMENT PRIMARY KEY, 
	TV_NAME VARCHAR(100) NOT NULL,
	TV_SS VARCHAR(50) NOT NULL,
	TV_COMPLETE BOOLEAN
);

CREATE TABLE MOVIES (
	MOVIES_ID INT(11) UNSIGNED AUTO_INCREMENT PRIMARY KEY, 
	MOVIES_NAME VARCHAR(100) NOT NULL,
	MOVIES_SS VARCHAR(50) NOT NULL,
	MOVIES_COMPLETE BOOLEAN
);

CREATE TABLE OTHER (
	OTHER_ID INT(11) UNSIGNED AUTO_INCREMENT PRIMARY KEY, 
	OTHER_NAME VARCHAR(100) NOT NULL,
	OTHER_SS VARCHAR(50) NOT NULL,
	OTHER_COMPLETE BOOLEAN
);